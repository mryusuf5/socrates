                    <div class="accordion-body">
                        <p>James Fadiman P.h.D. heeft een lijst opgesteld met medicijnen die volgens mensen die aan zijn onderzoek meegedaan hebben, zonder problemen gecombineerd kunnen worden met microdosing.
                            <a href="https://microdosing.nl/microdosing-101/veelgestelde-vragen/combineren-met-medicijnen-en-supplementen/">Klik hier</a>  om de lijst van medicijnen te bekijken.</p>
                        <p>Zoals met alles wat je tot je neemt zijn er altijd risico's. </p>
                        <h5>Gerapporteerde nadelen van microdosing:</h5>
                        <ul>
                            <li>Angstgevoelens kunnen toenemen. Dit is de reden waarom angstige personen vaker geen baat hebben bij microdosing.</li>
                            <li>Mensen met paranoia verschijnselen kunnen meer paranoia en argwaan ervaren tijdens microdoseren.</li>
                            <li>Microdosing truffels kunnen lichte maagklachten en misselijkheid geven bij gevoelige personen.</li>
                            <li>Enkele personen (mannen) met kleurenblindheid rapporteerden lichte hallucinaties, of nabeelden. Zij stopten hun cyclus. Dit geldt niet voor iedereen met kleurenblindheid</li>
                            <li>Mensen met Tinnitus kunnen meer last krijgen van oorsuizen door microdoseren.</li>
                            <li>Mensen met de diagnose bipolaire stoornis, meldden dat ze wel baat bij microdosing hadden tijdens hun depressieve fase, maar zouden het afraden tijdens een manische fase.</li>
                        </ul>
                        <h5>Voor wie wordt microdosing afgeraden?</h5>
                        <ul>
                            <li>Bij personen onder de 18 jaar.</li>
                            <li>Bij zwangerschap of als je borstvoeding geeft.</li>
                            <li>Zij die het medicijn lithiumcarbonaat (lithium) of Tramadol slikken.</li>
                            <li>Zij die psychose gevoelig zijn of ooit een psychose hebben gehad. </li>
                            <li>Mannen met kleurenblindheid. Zij kunnen ‘tracers’ gaan zien in hun gezichtsveld. Niet alle mensen met kleurenblindheid ervaren dit, maar de kans is aanwezig dat het voorkomt.</li>
                            <li>Mensen met een angststoornis, met uitzondering van sociale angst.</li>
                            <li>Mensen die aan paranoia/argwaan lijden. MD kan paranoia versterken.</li>
                        </ul>
